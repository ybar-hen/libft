#include "libft.h"

char	*ft_strncpy(char *s1, const char *s2, size_t n)
{
	char *rpce;

	rpce = s1;
	while(*s2 != '\0' && n > 0)
	{
		*s1 = *s2;
		s1++;
		s2++;
		n--;
	}
	while (n > 0)
	{
		*s1 = '\0';
		s1++;
		n--;
	}

	*s1 = '\0';
	return (rpce);
}
